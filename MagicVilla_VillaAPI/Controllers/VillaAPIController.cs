            //    Occupancy = villaDTO.Occupancy,
            //    Rate = villaDTO.Rate,
            //    sqft = villaDTO.sqft

            //};

            await _dbVilla.UpdateAsync(model);

            if (!ModelState.IsValid)
            {
                return BadRequest(ModelState);
            }

            return NoContent();

        }

